public class TipoDado {
    public static void  main(String[] args){
        //Demonstração de alguns tipos de dados
        int numero;
        float decimalCurto;
        double decimalLongo;
        char caracter;
        String cadeiaCaractere;

        numero = 10;
        decimalCurto = 10.5f;
        decimalLongo = 20.5;
        caracter = 'a';
        cadeiaCaractere = "Igor Carrilho";

        System.out.println(numero);
        System.out.println(decimalCurto);
        System.out.println(decimalLongo);
        System.out.println(cadeiaCaractere);
        System.out.println(caracter);
    }
}
